def split_sentences(text: str) -> list[str]:
    """按中英文句末标点分句，支持省略号、分号等。"""
    raw = re.split(r'(?<=[。！？!?…;；])\s*', text)
    sentences = [s.strip() for s in raw if s.strip()]
    return sentences if sentences else [text.strip()] if text.strip() else []
